
public class Maths3 
{
	public int Addition (int X, int Y)
	{
		int A;
		A = X + Y;
		return A;
	}
	
	public float Division (int X, int Y)
	{
		float A;
		A = X / Y;
		return A;
	}
	
	public int Subtraction (int X, int Y)
	{
		int A;
		A = X - Y;
		return A;
	}
	
	public int Multiplication (int X, int Y)
	{
		int A;
		A = X * Y;
		return A;
	}

}
